package com.ai.bdx.pop.exception;

public class PopInterfaceException extends PopException {
	public PopInterfaceException() {
		super();
	}

	public PopInterfaceException(String message) {
		super(message);
	}

	public PopInterfaceException(String message, Throwable cause) {
		super(message, cause);
	}

	public PopInterfaceException(Throwable cause) {
		super(cause);
	}
}
